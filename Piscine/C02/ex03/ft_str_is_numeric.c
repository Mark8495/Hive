int	ft_str_is_numeric(char *str)
{
	int	nb;

	nb = 0;
	while (str[nb] != '\0')
	{
		if (str[nb] >= '0' && str[nb] <= '9')
		{
			nb++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
